					<p>To form a circulated timestamp server as a peer-to-peer network, amazeecoin uses a proof-of-work system. The work in this system is what is often referred to as amazeecoin mining. The signature is exposed rather than provided by knowledge.</p>

					<p>This process is energy intensive. Electricity can consume more than 90% of operating costs for miners. A data centre are planned mostly for amazeecoin mining, is expected to require up to 135 MW of power.</p>

					<h4>Mining difficulty has increased extensively</h4>

					<p>To compensate for growing hardware speed and varying interest in running nodes over time, the difficulty of finding a valid hash is familiar roughly every two weeks. If blocks are generated too quickly, the complexity increases and more hashes are required to make a block and to generate new amazeecoins.</p>

					<h3>Difficulty</h3>

					<p>Amazeecoin mining is a competitive make an effort. An "arms race" has been observed through the various hashing technologies that have been used to mine amazeecoins: basic CPUs, high-end GPUs common in many gaming computers, FPGAs and ASICs all have been used, each reducing the profitability of the less-specialized technology. Amazeecoin-specific ASICs are now available. As amazeecoins become more difficult to mine, computer hardware manufacturing companies have seen an add to in sales of high-end products.</p>

					<p>Computing power is often bundled together or "pooled" to reduce variance in miner income. Individual mining rigs often have to wait for long periods to confirm a block of transactions and receive payment. In a pool, all participating miners get paid every time a participating server solves a block. This payment is proportional to the amount of work an individual miner contributed to help find that block.</p>

					<p>Amazeecoin data centers prefer to keep a low profile, are dispersed around the world and tend to cluster around the availability of cheap electricity.</p>

					<h3>Process</h3>

					<h5>A forceful overview of the process to mine amazeecoins is:</h5>

					<ul>

						<li>New transactions are broadcast to all nodes.</li>

						<li>Each miner node collects new transactions into a block.</li>

						<li>Each miner node works on decision a proof-of-work code for its block.</li>

						<li>When a node finds a proof-of-work, it broadcasts the block to all nodes.</li>

						<li>Receiving nodes confirm the transactions it holds and accept only if all are valid.</li>

						<li>Nodes express their approval by moving to work on the next block, incorporating the hash of the accepted block.</li>

					</ul>

					<h3>Mined amazeecoins</h3>

					<p>The first transaction in a block is a special transaction that produces new amazeecoins owned by the designer of the block. This is the motivation for nodes to support the network. It provides the way to move new amazeecoins into distribution. The reward for mining halves every 210,000 blocks. It started at 50 amazeecoin, drop to 25 in late 2012 and to 12.5 amazeecoin in 2016. This halving process is programmed to continue for 64 times before new coin creation ceases. </p>

					<h3>Payment verification</h3>

					<p>Each miner can choose which transactions are included in or exempted from a block. A greater number of transactions in a block does not equate to greater computational power required to solve that block. </p>

					<p>Upon receiving a new transaction a node must validate it: in particular, verify that none of the transaction's inputs have been formerly spent. To carry out that check the node needs to access the blockchain. several user who does not trust his network neighbors, should keep a full local copy of the blockchain, so that any input can be verified.</p>

